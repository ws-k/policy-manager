# ─── Tiptap 헬퍼 ──────────────────────────────────────────────
def h2(text):
    return {
        "type": "heading",
        "attrs": {"level": 2},
        "content": [{"type": "text", "text": text}],
    }


def bullets(items):
    return {
        "type": "bulletList",
        "content": [
            {
                "type": "listItem",
                "content": [
                    {
                        "type": "paragraph",
                        "content": [{"type": "text", "text": item}],
                    }
                ],
            }
            for item in items
        ],
    }


def doc(*nodes):
    return {"type": "doc", "content": list(nodes)}


# ─── Supabase 유틸 ────────────────────────────────────────────
def fetch_all_docs():
    url = f"{SUPABASE_URL}/rest/v1/policy_docs?select=id,content"
    req = urllib.request.Request(url, headers={**HEADERS, "Accept": "application/json"})
    with urllib.request.urlopen(req) as resp:
        return json.loads(resp.read().decode())


def extract_tables(content):
    """content JSON에서 type=='table' 노드를 순서대로 추출"""
    tables = []
    nodes = content.get("content", []) if isinstance(content, dict) else []
    for node in nodes:
        if node.get("type") == "table":
            tables.append(node)
    return tables


def patch_doc(doc_id, new_content):
    url = f"{SUPABASE_URL}/rest/v1/policy_docs?id=eq.{doc_id}"
    payload = json.dumps({"content": new_content}).encode("utf-8")
    req = urllib.request.Request(url, data=payload, headers=HEADERS, method="PATCH")
    try:
        with urllib.request.urlopen(req) as resp:
            return resp.status
    except urllib.error.HTTPError as e:
        return e.code


